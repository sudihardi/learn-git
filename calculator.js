// Write multiply, divide, sum, minus function
function multiply(a, b) {
    return a * b
}

function divide(a, b) {
    return a / b
}

function sum(a, b) {
    return a + b
}

function minus(a, b) {
    return a - b
}

function modulus(a, b) {
    return a % b
}

